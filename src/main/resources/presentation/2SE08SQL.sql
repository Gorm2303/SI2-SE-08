CREATE TABLE Organizations (
	id SERIAL PRIMARY KEY,
	name VARCHAR(100) NOT NULL
);

CREATE TABLE Productions (
	id SERIAL PRIMARY KEY,
	name VARCHAR(100) NOT NULL,
	releaseDate VARCHAR(10),
	length INT,
	producerId INT NOT NULL,
	FOREIGN KEY (producerId) REFERENCES Organizations(id)
);

CREATE TABLE OrganizationsInProductions (
	productionId INT NOT NULL,
	organizationId INT NOT NULL,
	FOREIGN KEY (productionId) REFERENCES Productions(id) ON DELETE CASCADE,
	FOREIGN KEY (organizationId) REFERENCES Organizations(id),
	PRIMARY KEY(productionId, organizationId)
);



CREATE TABLE Credits (
	id SERIAL PRIMARY KEY,
	role VARCHAR(100) NOT NULL,
	productionId INT NOT NULL,
	FOREIGN KEY (productionId) REFERENCES Productions(id) ON DELETE CASCADE
);

CREATE TABLE Contributors (
	id SERIAL PRIMARY KEY,
	name VARCHAR(50) NOT NULL,
	birthDate VARCHAR(10)
);

CREATE TABLE ContributorsInCredits (
	creditId INT NOT NULL,
	contributorId INT NOT NULL,
	FOREIGN KEY (creditId) REFERENCES Credits(id) ON DELETE CASCADE,
	FOREIGN KEY (contributorId) REFERENCES Contributors(id),
	PRIMARY KEY (creditId, contributorId)
);





